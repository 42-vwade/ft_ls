#include "ft_ls.h"

void	ls_endslash(char path[PATH_MAX])
{
	int		i;

	i = ft_strlen(path);
	if (path[i - 1] != '/' && i + 1 < PATH_MAX)
	{
		path[0] = '/';
		path[1] = 0;
	}
}
